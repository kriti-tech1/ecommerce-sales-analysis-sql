--q13)Find customers whose average order value is greater than the overall average order value.
WITH total_cte AS (
SELECT 
	c.customer_id ,
	c.customer_name ,
	(SUM(od.quantity*p.price)/count(DISTINCT(od.order_id)))::DECIMAL(10,2) AS avg_order_value
FROM 	
	customers c
JOIN 
	orders o
ON c.customer_id = o.customer_id 
JOIN 
	order_details od
ON od.order_id = o.order_id 
JOIN 
	products p
ON p.product_id = od.product_id 
GROUP BY c.customer_name,c.customer_id
)
SELECT
 	t.customer_name,
	t.avg_order_value
FROM
 total_cte t
 WHERE t.avg_order_value >(
		SELECT 
				AVG(t.avg_order_value)
		FROM
			total_cte t
);

--q14)For each category, find the highest revenue-generating product.
WITH max_revenue AS(
SELECT 
	p.product_id,
 	p.category,
	p.product_name,
	SUM(od.quantity*p.price)AS revenue,
	DENSE_RANK() OVER(PARTITION BY p.category ORDER BY (SUM(od.quantity*p.price)) DESC) AS ranking
FROM 
	order_details od 
JOIN
	products p
ON p.product_id = od.product_id
GROUP BY p.product_id,p.product_name,p.category
) 
SELECT
	m.category,
	m.product_name,
	m.revenue
FROM
	max_revenue m
WHERE m.ranking =1 
ORDER BY m.revenue DESC;

