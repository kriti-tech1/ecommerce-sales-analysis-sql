--q1)Find the total revenue generated by each product.
WITH revenue_cte AS(
	 SELECT
	    product_id ,
	   SUM(quantity ) AS real_quantity
	 FROM 
	    order_details 
	GROUP BY product_id 
)		
SELECT		
		p.product_name,
		(p.price)*(rc.real_quantity) AS total_revenue 
	FROM 
		products p
	JOIN
		revenue_cte rc
	ON p.product_id = rc.product_id
ORDER BY total_revenue DESC; 

--q2)Find the customer who generated the highest total revenue.
WITH rev_cte AS(
SELECT 
	c.customer_name ,
	c.customer_id ,
	p.price ,
	od.quantity
FROM 
   customers c
JOIN 
	orders o
ON c.customer_id = o.customer_id
JOIN 
    order_details od
ON od.order_id = o.order_id 
JOIN 
	products p
ON od.product_id = p.product_id 
  
),

  rank_cte AS(
SELECT 
   rc.customer_id,
   rc.customer_name,
   SUM(rc.price * rc.quantity) AS total_revenue,
   DENSE_RANK()OVER(ORDER BY(SUM(rc.price * rc.quantity)) DESC) AS ranking
FROM 
   rev_cte rc
GROUP BY rc.customer_name, rc.customer_id

)
SELECT * FROM rank_cte 
WHERE ranking =1 ;
	  	
	
--Q3)Find customers who placed more than 1 order.
SELECT 
		  c.customer_id ,
		  c.customer_name,
	      COUNT(o.order_id) AS total_orders 
FROM
	customers c
JOIN 
	orders o 
ON o.customer_id = c.customer_id 
GROUP BY c.customer_id , c.customer_name
HAVING COUNT(o.order_id)> 1;

--q4)Find total revenue generated by each product category.
SELECT 
      p.category,
	  SUM(od.quantity *p.price ) AS total_revenue
FROM 
	products p
JOIN
	order_details od
ON p.product_id = od.product_id
GROUP BY p.category
ORDER BY total_revenue DESC;

--q5)Find customers who purchased products from more than 1 category.
SELECT 
	c.customer_name,
	COUNT(DISTINCT(p.category)) AS distinct_category
FROM 
 	customers c
JOIN 
	orders o
ON o.customer_id = c.customer_id 
JOIN 
   order_details od
ON od.order_id = o.order_id 
JOIN 
	products p
ON p.product_id = od.product_id 
GROUP BY c.customer_name 
HAVING COUNT(DISTINCT(p.category)) > 1;

--q6)Rank product categories based on total revenue generated.
SELECT 
		p.category,
		SUM(p.price * od.quantity) AS total_revenue ,
		DENSE_RANK() OVER (ORDER BY (SUM(p.price * od.quantity)) DESC)  AS ranking
FROM 
	products p
JOIN 
	order_details od 
ON p.product_id = od.product_id 
GROUP BY p.category;

--q8)Find customers whose total spending is greater than the average spending of all customers.
WITH total_spend AS (
SELECT
	c.customer_id,
	c.customer_name ,
	SUM(p.price * od.quantity) AS total_spending 
FROM 
	customers c
JOIN 
	orders o
ON c.customer_id = o.customer_id 
JOIN
	order_details od
ON od.order_id = o.order_id 
JOIN
 	products p
ON p.product_id = od.product_id 
GROUP BY c.customer_id,c.customer_name 
)
SELECT 
     t.customer_name ,
	 t.total_spending
FROM 
	total_spend t
WHERE 
t.total_spending >(
	SELECT 
	(AVG(t.total_spending)) 
	FROM total_spend t
)
ORDER BY t.total_spending DESC;

--q8)For each customer, show their most expensive purchased product.
WITH rank_cte AS (
SELECT 
		c.customer_id,
		c.customer_name ,
		p.product_name ,
		p.price ,
		DENSE_RANK() OVER(PARTITION BY c.customer_name,c.customer_id  ORDER BY p.price DESC) AS ranking 
FROM 
	customers c
JOIN 
	orders o
ON c.customer_id = o.customer_id 
JOIN
	order_details od
ON od.order_id = o.order_id 
JOIN
 	products p
ON p.product_id = od.product_id 
)
SELECT 
	r.customer_name ,
	r.product_name ,
	r.price 
FROM 
	rank_cte r
WHERE r.ranking = 1
ORDER BY r.price DESC;

--q9)Show each customer’s first order date and latest order date.
SELECT
	c.customer_id,
	c.customer_name,
	MIN(o.order_date ) AS first_order,
	MAX(o.order_date) AS latest_order
FROM	
	customers c
JOIN
	orders o
ON o.customer_id = c.customer_id
GROUP BY c.customer_id,c.customer_name
ORDER BY c.customer_id ASC;

--q10)Find customers whose total spending is higher than every customer from Delhi.
WITH spend_cte AS(
SELECT 
	c.customer_id,
	c.customer_name ,
	c.city,
	SUM(p.price*od.quantity)AS total_spending
FROM
	customers c
JOIN 
	orders o
ON o.customer_id = c.customer_id 
JOIN 
	order_details od 
ON od.order_id = o.order_id 
JOIN
	products p
ON p.product_id=od.product_id 
GROUP BY c.customer_id ,c.customer_name,c.city
)
SELECT 
	s.customer_name ,
	s.total_spending 
FROM 
 	spend_cte s
WHERE s.total_spending >ALL(
        SELECT
       		s.total_spending
		FROM 
			spend_cte s
		WHERE s.city = 'Delhi'
		
);

--q11)Show running total revenue over time based on order date.
WITH time_cte AS(
SELECT
	o.order_date ,
	SUM(p.price * od.quantity)  AS daily_revenue
FROM 
	orders o
JOIN 
	order_details od 
ON o.order_id = od.order_id 
JOIN
	products p
ON p.product_id = od.product_id
GROUP BY o.order_date
ORDER BY o.order_date ASC
)
SELECT
	t.order_date ,
	t.daily_revenue,
	SUM(t.daily_revenue) OVER (ORDER BY t.order_date ) AS running_revenue
FROM 
	time_cte t;

